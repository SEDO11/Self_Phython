list_a = [273, 32, 103, 57, 52, "오세훈", True]

# in 연산자
print("in 연산자")
print(273 in list_a)
print(2 in list_a)
print("오세훈" in list_a)
print("홍길동" in list_a)
print(False in list_a)
print(True in list_a)
print()

# not in 연산자
print("not in 연산자")
print(273 not in list_a)
print(2 not in list_a)
print("오세훈" not in list_a)
print("홍길동" not in list_a)
print(False not in list_a)
print(True not in list_a)